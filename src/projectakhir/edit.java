    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        File file = editGambar.getSelectedFile();
        try {
            // Mengkonversi gambar ke byte array
            FileInputStream fis = new FileInputStream(file);
            byte[] imageBytes = new byte[(int) file.length()];
            fis.read(imageBytes);

            // Menyimpan gambar ke database
            saveImageToDatabase(imageBytes);
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, "Error membaca file gambar: " + ex.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

