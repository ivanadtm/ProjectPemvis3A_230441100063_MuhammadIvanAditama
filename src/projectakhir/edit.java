    private void saveImageToDatabase(byte[] imageBytes) {
        String sql = "INSERT INTO makanan (id_makanan, nama_makanan, deskripsi, gambar) VALUES (?, ?, ?)";  // Pastikan ada kolom 'gambar' bertipe BLOB
        try {
            PreparedStatement ps = conn.prepareStatement(sql);
            // Ganti dengan nama yang sesuai dari input pengguna, jika diperlukan
            ps.setString(1, admin.idMakanan.getText());
            ps.setString(1, editNama.getText());
            ps.setString(2, editDeskripsi.getText());
            ps.setBytes(3, imageBytes);
            int rowsAffected = ps.executeUpdate();
            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(null, "Gambar berhasil disimpan!");
            } else {
                JOptionPane.showMessageDialog(null, "Gagal menyimpan gambar.");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error menyimpan gambar ke database: " + e.getMessage());
        }
    }
    
