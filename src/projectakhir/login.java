    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String username = txtAdmin.getText();
        String password = txtPassAdmin.getText(); // Mengambil password
        int idAdmin = validateLoginAdmin(username, password);
        admin admin = new admin();
        
        if (idAdmin != -1) {
            JOptionPane.showMessageDialog(this, "Login Berhasil!" + idAdmin);
            // Jika login berhasil, buka form utama aplikasi atau pindah ke frame lain
            // Misalnya, new MainForm().setVisible(true);
            Session.setUserId(idAdmin);
            admin.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Username atau Password salah.");
        }
        
        
        DefaultTableModel model = new DefaultTableModel();
        admin.tblMakanan.setModel(model);

        model.addColumn("Id_makanan");
        model.addColumn("Nama_makanan");
        model.addColumn("Deskripsi");
        model.addColumn("Gambar");
        model.addColumn("Rating");
        
        model.setRowCount(0);

        try {
            String sql = "SELECT id_makanan, nama_makanan, deskripsi, gambar, rating FROM makanan";
            PreparedStatement ps = conn.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
               // Menambahkan baris ke dalam model tabel
                int id = rs.getInt("id_makanan");
                String nama = rs.getString("nama_makanan");
                String deskripsi = rs.getString("deskripsi");
                byte[] gambarBytes = rs.getBytes("gambar");
                int rating = rs.getInt("rating");

//                Konversi byte[] ke ImageIcon;
                ImageIcon gambar = null;
                if (gambarBytes != null) {
                    gambar = new ImageIcon(gambarBytes);
                }

                // Tambahkan data ke tabel
                model.addRow(new Object[]{id, nama, deskripsi,gambar, rating});
            }
        } catch (SQLException e) {
           System.out.println("Error Save Data" + e.getMessage());
         }
        
        DefaultTableModel modelU = new DefaultTableModel();
        admin.tblUser.setModel(modelU);

        modelU.addColumn("Id_user");
        modelU.addColumn("Nama_user");
        modelU.addColumn("Email");
        modelU.addColumn("Password");
        
        modelU.setRowCount(0);

        try {
            String sql = "SELECT * FROM login";
            PreparedStatement ps = conn.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
               // Menambahkan baris ke dalam model tabel
               modelU.addRow(new Object[]{
               rs.getInt("id_user"),
               rs.getString("nama_user"),
               rs.getString("email"),
               rs.getString("password")
             });
            }
        } catch (SQLException e) {
           System.out.println("Error Save Data" + e.getMessage());
         }
    }//GEN-LAST:event_jButton3ActionPerformed

