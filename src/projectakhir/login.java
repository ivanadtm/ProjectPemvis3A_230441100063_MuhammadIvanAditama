    private int validateLogin(String username, String password){
        String sql = "SELECT id_user FROM login WHERE email = ? AND password = ?";
            try (PreparedStatement ps = conn.prepareStatement(sql)) {
                ps.setString(1, username);
                ps.setString(2, password); // Anda bisa mengenkripsi password menggunakan hash di sini
                ResultSet rs = ps.executeQuery();

            if (rs.next()){
                return rs.getInt("id_user");
            }// Login berhasil
            // Login gagal
            } catch (SQLException e) {
                e.getMessage();
        }
            return -1;
    }
    private int validateLoginAdmin(String username, String password){
        String sql = "SELECT id_admin FROM admin WHERE email_admin = ? AND password_admin = ?";
            try (PreparedStatement ps = conn.prepareStatement(sql)) {
                ps.setString(1, username);
                ps.setString(2, password); // Anda bisa mengenkripsi password menggunakan hash di sini
                ResultSet rs = ps.executeQuery();

            if(rs.next()){
                return rs.getInt("id_admin");
            }// Login berhasil
            // Login gagal
            } catch (SQLException e) {
                System.out.println("Error Save Data" + e.getMessage());
        }
            return -1;
    }
    
    private void saveDataRegist(){
        if(txtNama.getText().isEmpty()|| txtPw.getText().isEmpty()||txtEmail.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Salah satu form masih kosong", "peringatan", JOptionPane.ERROR_MESSAGE);
        }else{
            try{
            String sql = "INSERT INTO login (nama_user, email, password) VALUES (?, ?, ?)";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, txtNama.getText());
            ps.setString(2, txtEmail.getText());
            ps.setString(3, txtPw.getText());
            ps.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data saved successfully");
          } catch (SQLException e) {
            System.out.println("Error Save Data" + e.getMessage());
          }
        }
    }
    
